export function validateYaml(data: any) {
  if (!schema) return true;
  const validate = ajv.compile(schema);
  const valid = validate(data);
  if (!valid) {
    console.error("Validation errors:", validate.errors);
  }
  return valid;
}

export function resolveCommand(action: string | { [platform: string]: string } | undefined): string {
  if (!action) return "";
  if (typeof action === 'string') return action;
  const platform = process.platform;
  if (action[platform]) return action[platform];
  if (action['default']) return action['default'];
  const firstKey = Object.keys(action).find(k => k !== 'default');
  if (firstKey) return action[firstKey];
  return action['default'] || "";
}

export function loadTree(filePath: string): Tree {
  const content = fs.readFileSync(filePath, 'utf8');
  const source = yaml.load(content) as any;
  if (Array.isArray(source)) {
    validateYaml(source);
    return tableToTree(source, path.dirname(filePath));
  } else {
    return resolveBundles(source, path.dirname(filePath));
  }
}

export class ZshStrategy implements ConfigStrategy {
  id = 'zsh';
  mnemonicSeparator = '';
  getMnemonic(parent: string, key: string): string { return parent + key; }
  getEntryName(mnemonic: string, slugParts: string[]): string {
    const cleanSlug = slugParts.map(s => s.replace(/\s+/g, '-')).join('-');
    return `${mnemonic}-${cleanSlug}`;
  }
  splitMnemonic(mnemonic: string): string[] { return mnemonic.split(''); }
  getSourceCommand(outputPath: string): string { return `source ${outputPath}`; }
  render(tree: Tree): string {
    const allEntries = getAllEntries(tree, this);
    const mnemonicMap: { [key: string]: Entry[] } = {};
    allEntries.forEach(entry => {
      if (!mnemonicMap[entry.mnemonic]) mnemonicMap[entry.mnemonic] = [];
      mnemonicMap[entry.mnemonic].push(entry);
    });
    const output: string[] = ["# Generated from extend config", "# Do not edit this file directly", ""];
    const mnemonics = Object.keys(mnemonicMap);
    mnemonics.forEach(m => {
      try {
        const originalPath = execSync(`which ${m}`, { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim();
        if (originalPath && !originalPath.includes('not found')) {
          output.push(`alias extend_original_${m}='${originalPath}'`);
        }
      } catch (e) { }
    });
    output.push("");
    allEntries.forEach(entry => {
      let cmd = entry.cmd;
      mnemonics.forEach(m => {
        const regex = new RegExp(`(^|\\s)${m}(\\s|$)`, 'g');
        if (regex.test(cmd)) {
          if (output.some(line => line.startsWith(`alias extend_original_${m}=`))) {
            cmd = cmd.replace(regex, `$1extend_original_${m}$2`);
          }
        }
      });
      switch (entry.type) {
        case 'export': output.push(cmd); break;
        case 'func':
          const names = [entry.name];
          if (entry.mnemonic !== entry.name && mnemonicMap[entry.mnemonic][0] === entry) {
            names.push(entry.mnemonic);
          }
          const namesStr = names.join(' ');
          if (cmd.includes('\n')) {
            output.push(`function ${namesStr} {`);
            cmd.split('\n').forEach(line => output.push(`  ${line}`));
            output.push(`}`);
          } else { output.push(`function ${namesStr} { ${cmd} }`); }
          break;
        case 'alias':
          output.push(`alias ${entry.name}='${cmd}' ${entry.comment}`);
          if (mnemonicMap[entry.mnemonic][0] === entry) output.push(`alias ${entry.mnemonic}='${cmd}'`);
          break;
      }
    });
    return output.join("\n");
  }
  sync() {
    const config = loadConfig();
    const zshConfig = config.renderers.zsh;
    if (!zshConfig || !zshConfig.enabled) return;
    const tree = loadTree(zshConfig.source_path);
    const content = this.render(tree);
    fs.writeFileSync(zshConfig.output_path, content);
    const allEntries = getAllEntries(tree, this);
    let maxMnemonic = 0, maxLabel = 0;
    allEntries.forEach(e => {
      const label = e.category ? `${e.category} -> ${e.description}` : e.description;
      if (e.mnemonic.length > maxMnemonic) maxMnemonic = e.mnemonic.length;
      if (label.length > maxLabel) maxLabel = label.length;
    });
    const hintLines = allEntries.map(e => {
      const label = e.category ? `${e.category} -> ${e.description}` : e.description;
      return `${e.mnemonic.padEnd(maxMnemonic)} │ ${label.padEnd(maxLabel)}`;
    });
    fs.writeFileSync(path.join(CONFIG_DIR, 'zsh_fzf_hints.txt'), hintLines.join("\n"));
  }
}

export class TmuxStrategy implements ConfigStrategy {
  id = 'tmux';
  mnemonicSeparator = '->';
  getMnemonic(parent: string, key: string): string { return parent ? `${parent}${this.mnemonicSeparator}${key}` : key; }
  getEntryName(mnemonic: string, slugParts: string[]): string { return slugParts.filter(s => s).map(s => s.replace(/\s+/g, '-')).join('-'); }
  splitMnemonic(mnemonic: string): string[] { return mnemonic.split(this.mnemonicSeparator); }
  getSourceCommand(outputPath: string): string { return `tmux source-file ${outputPath}`; }
  render(tree: Tree): string {
    const config = loadConfig();
    const tmuxConfig = config.renderers.tmux;
    const prefix = tmuxConfig?.prefix;
    let output = generateTmux(config.renderers.tmux.source_path);
    if (prefix) output = `bind-key -n ${prefix} switch-client -Troot\n` + output;
    return output;
  }
  sync() {
    const config = loadConfig();
    const tmuxConfig = config.renderers.tmux;
    if (!tmuxConfig || !tmuxConfig.enabled) return;
    const content = this.render({} as Tree);
    fs.writeFileSync(tmuxConfig.output_path, content);
  }
}

export class HammerspoonStrategy implements ConfigStrategy {
  id = 'hammerspoon';
  mnemonicSeparator = '';
  getMnemonic(parent: string, key: string): string { return parent + key; }
  getEntryName(mnemonic: string, slugParts: string[]): string { return slugParts.filter(s => s).map(s => s.replace(/\s+/g, '-')).join('-'); }
  splitMnemonic(mnemonic: string): string[] { return mnemonic.split(''); }
  getSourceCommand(outputPath: string): string { return `dofile("${outputPath}")`; }
  render(tree: Tree): string {
    const config = loadConfig();
    const hsConfig = config.renderers.hammerspoon;
    const leader = hsConfig?.prefix || "control-space";
    let leaderMod: string = "", leaderKey = "";
    if (leader.includes('-')) { const parts = leader.split('-'); leaderKey = parts.pop() || ""; leaderMod = parts + ""; }
    else { leaderKey = leader; }
    const output: string[] = ["-- Generated from extend config", "-- Do not edit this file directly", "", `local root = hs.hotkey.modal.new(${JSON.stringify(leaderMod)}, ${JSON.stringify(leaderKey)})`];
    const rootNodes: { [key: string]: Node } = {};
    Object.values(tree).forEach(nodes => {
      Object.entries(nodes).forEach(([key, node]) => {
        if (rootNodes[key]) {
          if (node.children) { if (!rootNodes[key].children) rootNodes[key].children = {}; Object.assign(rootNodes[key].children!, node.children); }
          if (node.exec) rootNodes[key].exec = node.exec;
        } else { rootNodes[key] = node; }
      });
    });
    if (hsConfig?.debug) {
      output.push("function root:entered() ", "  hs.alert.show('Extend Mode')", "  print('--- Extend Mode Root ---')");
      Object.entries(rootNodes).forEach(([key, node]) => { output.push(`  print('${key}: ${node.description || key}')`); });
      output.push("end");
    } else { output.push("function root:entered() hs.alert.show('Extend Mode') end"); }
    output.push("function root:exited() hs.alert.closeAll() end", 'root:bind("", "escape", function() root:exit() end)', "");

    const walk = (node: Node, modalStack: string[], modalPath: string) => {
      const parentModal = modalStack[modalStack.length - 1];
      if (node.children) {
        Object.entries(node.children).forEach(([key, child]) => {
          if (child.exec) {
            const cmd = resolveCommand(child.exec);
            const desc = child.description || key;
            const type = child.type;
            output.push(`${parentModal}:bind("", ${JSON.stringify(key)}, ${JSON.stringify(desc)}, function()`);
            if (type === 'function' || type === 'func' || cmd.includes('hs.') || cmd.includes('(')) { output.push(`  ${cmd.replace(/\n/g, '\n  ')}`); }
            else { output.push(`  hs.execute(${JSON.stringify(cmd)})`); }
            modalStack.slice().reverse().forEach(m => { output.push(`  ${m}:exit()`); });
            output.push("end)");
          } else if (child.children) {
            const modalName = `modal_${parentModal}_${key}`.replace(/[^a-zA-Z0-9_]/g, '_');
            const currentPath = `${modalPath} -> ${child.description || key}`;
            output.push(`local ${modalName} = hs.hotkey.modal.new()`);
            if (hsConfig?.debug) {
              output.push(`function ${modalName}:entered()`, `  print('--- ${currentPath} ---')`);
              Object.entries(child.children).forEach(([k, v]) => { output.push(`  print('${k}: ${v.description || k}')`); });
              output.push("end");
            }
            output.push(`${parentModal}:bind("", ${JSON.stringify(key)}, ${JSON.stringify(child.description || key)}, function() ${modalName}:enter() end)`);
            output.push(`${modalName}:bind("", "escape", function()`);
            modalStack.slice().reverse().forEach(m => { output.push(`  ${m}:exit()`); });
            output.push(`  ${modalName}:exit()`);
            output.push("end)");
            walk(child, [...modalStack, modalName], currentPath);
          }
        });
      }
    };
    Object.entries(rootNodes).forEach(([key, node]) => {
      if (node.exec) {
        const cmd = resolveCommand(node.exec);
        const type = node.type;
        output.push(`root:bind("", ${JSON.stringify(key)}, ${JSON.stringify(node.description || key)}, function()`);
        if (type === 'function' || type === 'func' || cmd.includes('hs.') || cmd.includes('(')) { output.push(`  ${cmd.replace(/\n/g, '\n  ')}`); }
        else { output.push(`  hs.execute(${JSON.stringify(cmd)})`); }
        output.push(`  root:exit()`, "end)");
      } else if (node.children) {
        const modalName = `modal_root_${key}`.replace(/[^a-zA-Z0-9_]/g, '_');
        const currentPath = `${node.description || key}`;
        output.push(`local ${modalName} = hs.hotkey.modal.new()`);
        if (hsConfig?.debug) {
          output.push(`function ${modalName}:entered()`, `  print('--- ${currentPath} ---')`);
          Object.entries(node.children).forEach(([k, v]) => { output.push(`  print('${k}: ${v.description || k}')`); });
          output.push("end");
        }
        output.push(`root:bind("", ${JSON.stringify(key)}, ${JSON.stringify(node.description || key)}, function() ${modalName}:enter() end)`);
        output.push(`${modalName}:bind("", "escape", function()`, `  ${modalName}:exit()`, `  root:exit()`, "end)");
        walk(node, ["root", modalName], currentPath);
      }
    });
    return output.join("\n");
  }
  sync() {
    const config = loadConfig();
    const hsConfig = config.renderers.hammerspoon;
    if (!hsConfig || !hsConfig.enabled) return;
    const tree = loadTree(hsConfig.source_path);
    const content = this.render(tree);
    fs.writeFileSync(hsConfig.output_path, content);
  }
}

