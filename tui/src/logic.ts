export interface AppConfig {
  renderers: {
    [key: string]: {
      enabled: boolean;
      source_path: string;
      output_path: string;
      prefix?: string;
      debug?: boolean;
    }
  }
}

export function loadConfig(): AppConfig {
  if (!fs.existsSync(CONFIG_JSON)) {
    return {
      renderers: {
        zsh: {
          enabled: true,
          source_path: path.join(CONFIG_DIR, 'zsh.extend.yml'),
          output_path: path.resolve(process.env.HOME || '', '.extend.zsh')
        },
        tmux: {
          enabled: true,
          source_path: path.join(CONFIG_DIR, 'tmux.extend.yml'),
          output_path: path.resolve(process.env.HOME || '', '.tmux.extend.conf')
        }
      }
    };
  }
  const content = fs.readFileSync(CONFIG_JSON, 'utf8');
  const config = JSON.parse(content) as AppConfig;
  Object.values(config.renderers).forEach(r => {
    r.source_path = r.source_path.replace(/^~/, process.env.HOME || '');
    r.output_path = r.output_path.replace(/^~/, process.env.HOME || '');
  });
  return config;
}

