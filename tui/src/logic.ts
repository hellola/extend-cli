function resolveBundles(tree: any, baseDir: string, parentTable?: string, parentDescription?: string): any {
  if (typeof tree !== 'object' || tree === null) return tree;
  const resolved: any = Array.isArray(tree) ? [] : {};
  for (const [key, value] of Object.entries(tree)) {
    let node = value as any;
    if (node && typeof node === 'object' && node.bundle) {
      const bundlePath = path.resolve(baseDir, node.bundle);
      if (fs.existsSync(bundlePath)) {
        const bundleContent = fs.readFileSync(bundlePath, 'utf8');
        const bundleData = yaml.load(bundleContent) as any;
        node = { ...node, ...bundleData, _source: bundlePath };
        delete node.bundle;
      }
    }
    if (node && typeof node === 'object') {
      const currentTable = node.tableName || parentTable || (parentTable === undefined ? key : undefined);
      const currentDescription = node.tableDescription || parentDescription || (parentDescription === undefined ? (node.description || key) : undefined);
      resolved[key] = resolveBundles(node, baseDir, currentTable, currentDescription);
      if (resolved[key] && typeof resolved[key] === 'object') {
        if (resolved[key].tableName === undefined) resolved[key].tableName = currentTable;
        if (resolved[key].tableDescription === undefined) resolved[key].tableDescription = currentDescription;
      }
    } else { resolved[key] = node; }
  }
  return resolved;
}

export function generateTmux(filePath: string): string {
  const content = fs.readFileSync(filePath, 'utf8');
  const smartSwitch = path.resolve(__dirname, '../../smart_session_switch.rb');
  const helperScript = path.resolve(__dirname, '../../tmux_mode_helper.rb');
  let dataContent = content.replace(/#\{smart_session_switch\}/g, smartSwitch);
  const rawData = yaml.load(dataContent) as TableSource;
  const data = resolveBundlesForTmux(rawData, path.dirname(filePath));
  const tableMnemonics = calculateTableMnemonics(data);
  const allBindsForPalette: string[] = [];
  const allGenerated = data.map(entry => {
    if (!entry) return "";
    const [name, tableData] = Object.entries(entry)[0];
    const modal = tableData.modal === true;
    const validBinds = tableData.binds.filter(bind => { if (!bind.dependency) return true; try { execSync(`which ${bind.dependency}`, { stdio: 'ignore' }); return true; } catch (e) { return false; } });
    const tableMnemonic = tableMnemonics[name] || "";
    const tableDescription = tableData.description || name;
    const hintLines = validBinds.map(b => { if (name !== 'root') { allBindsForPalette.push(formatAllBind(name, tableDescription, tableMnemonic, b)); } return formatBind(b); });
    if (!fs.existsSync(HINTS_DIR)) fs.mkdirSync(HINTS_DIR, { recursive: true });
    fs.writeFileSync(path.join(HINTS_DIR, `${name}.txt`), hintLines.join("\n"));
    const lines: string[] = [];
    lines.push(`# table: ${name}`, `# modal: ${modal}`);
    if (name !== 'root' && name !== 'prefix') {
      lines.push(`if-shell 'tmux list-keys -T${name} >/dev/null 2>&1' 'unbind-key -a -T${name}'`);
      const rubyBin = process.platform === 'darwin' ? '/System/Library/Frameworks/Ruby.framework/Versions/2.6/usr/bin/ruby' : 'ruby';
      lines.push(`bind-key -T${name} ? display-popup -E -w 80% -h 40% "${rubyBin} --disable-gems ${helperScript} fuzzy ${name}"`);
    }
    validBinds.forEach(bind => {
      const key = bind.key;
      let action = (name === 'extend' && key === 'h') ? `display-popup -E -w 90% -h 60% "/usr/bin/ruby --disable-gems ${helperScript} palette"` : getTmuxAction(bind, name, modal);
      lines.push(`bind-key -T${name} ${key} ${action}`);
    });
    return lines.join("\n");
  });
  fs.writeFileSync(path.join(HINTS_DIR, "all.txt"), allBindsForPalette.join("\n"));
  return allGenerated.join("\n");
}

function resolveBundlesForTmux(entries: TableSource, baseDir: string): TableSource {
  const resolved: TableSource = [];
  const queue = [...entries];
  const processed: { [key: string]: boolean } = {};
  while (queue.length > 0) {
    const entry = queue.shift()!; if (!entry) continue;
    const [name, tableData] = Object.entries(entry)[0];
    if (processed[name]) continue;
    if (tableData.mount === false) { processed[name] = true; continue; }
    processed[name] = true;
    const newBinds: TableBind[] = [];
    if (tableData && tableData.binds) {
      tableData.binds.forEach(bind => {
        if (bind.mount === false) return;
        if (bind.bundle) {
          const bundlePath = path.resolve(baseDir, bind.bundle);
          if (fs.existsSync(bundlePath)) {
            const bundleContent = yaml.load(fs.readFileSync(bundlePath, 'utf8')) as any;
            const bundleName = path.basename(bundlePath, '.extend.yml');
            newBinds.push({ key: bind.key, table: bundleName, description: bind.description || bundleContent.description || bundleName });
            if (!processed[bundleName] && !queue.find(e => e && Object.keys(e)[0] === bundleName)) {
              const bundleTableBinds: TableBind[] = [];
              if (bundleContent.children) {
                Object.entries(bundleContent.children).forEach(([k, v]: [string, any]) => {
                  if (typeof v === 'object') { bundleTableBinds.push({ key: k, type: v.exec ? 'exec' : (v.send ? 'send' : undefined), action: v.exec || v.send || v.action, description: v.description }); }
                  else { bundleTableBinds.push({ key: k, action: v }); }
                });
              }
              queue.push({ [bundleName]: { binds: bundleTableBinds, modal: true, description: bundleContent.description } });
            }
          }
        } else { newBinds.push(bind); }
      });
    }
    resolved.push({ [name]: { ...tableData, binds: newBinds } });
  }
  return resolved;
}

function calculateTableMnemonics(data: TableSource): { [tableName: string]: string } {
  const tableToMnemonic: { [name: string]: string } = { root: "", prefix: "" };
  const queue = ["root", "prefix"];
  const visited = new Set(queue);
  const strategy = STRATEGIES.tmux;
  while (queue.length > 0) {
    const currentName = queue.shift()!;
    const entry = data.find(e => e && Object.keys(e)[0] === currentName);
    if (!entry) continue;
    const tableData = entry[currentName];
    const currentMnemonic = tableToMnemonic[currentName];
    tableData.binds.forEach(bind => {
      if (bind.table) {
        const nextMnemonic = strategy.getMnemonic(currentMnemonic, bind.key);
        if (!tableToMnemonic[bind.table]) {
          tableToMnemonic[bind.table] = nextMnemonic;
          if (!visited.has(bind.table)) { visited.add(bind.table); queue.push(bind.table); }
        }
      }
    });
  }
  return tableToMnemonic;
}

function formatBind(bind: TableBind): string {
  const key = bind.key;
  const action = bind.action || `Mode: ${bind.table}`;
  const desc = bind.description || "";
  return `${key.padEnd(8)} │ ${desc.padEnd(35)} │ ${action}`;
}

function formatAllBind(tableName: string, tableDescription: string, tableMnemonic: string, bind: TableBind): string {
  const key = bind.key;
  const action = bind.action || `Mode: ${bind.table}`;
  const desc = bind.description || "";
  const strategy = STRATEGIES.tmux;
  const fullMnemonic = strategy.getMnemonic(tableMnemonic, key);
  return `${fullMnemonic.padEnd(25)} │ ${desc.padEnd(45)} │ ${tableDescription.padEnd(20)} │ ${tableName}:${key} │ ${action}`;
}

function getTmuxAction(bind: TableBind, tableName: string, isModal: boolean): string {
  let action = resolveCommand(bind.action);
  if (!action && bind.table) { return `switch-client -T${bind.table} \\; display-message '${bind.table}'`; }
  switch (bind.type) {
    case 'exec': action = `send-keys ${escapeShell(action)} Enter`; break;
    case 'send': action = `send-keys ${escapeShell(action)}`; break;
    case 'test': action = `display-message ${escapeShell(action)}`; break;
    case 'run': action = `run -b ${escapeShell(action)}`; break;
  }
  if (isModal) { action += ` \\; switch-client -T${tableName}`; }
  return action;
}

function escapeShell(cmd: string): string { return `"${cmd}"` }
